public class Addition {
    public static void main(String[] args) {
        // Declare two variables
        int num1 = 10;
        int num2 = 20;
        
        // Calculate the sum
        int sum = num1 + num2;
        
        // Display the result
        System.out.println("Number 1: " + num1);
        System.out.println("Number 2: " + num2);
        System.out.println("Sum: " + sum);
    }
}
